    public void rellenarTable(){
        DefaultTableModel model = (DefaultTableModel) materiales.getModel();
        model.setRowCount(0);
        for (Material material : listaDemateriales) {
            model.addRow(new Object[]{material.getNombre(), ""});
        }
    }

